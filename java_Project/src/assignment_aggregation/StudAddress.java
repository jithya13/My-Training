package assignment_aggregation;

public class StudAddress {

	String city;
	String state;
	String country;
	int zipcode;
	
	public StudAddress(String city, String state, String country, int zipcode) {
		super();
		this.city = city;
		this.state = state;
		this.country = country;
		this.zipcode = zipcode;
	}
}
